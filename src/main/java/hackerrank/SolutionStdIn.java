package hackerrank;

import java.util.Scanner;

public class SolutionStdIn {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        String myString = scanner.next();
        int myInt_1 = scanner.nextInt();
        int myInt_2 = scanner.nextInt();
        int myInt_3 = scanner.nextInt();

        System.out.println("My String is: " + myString);
        System.out.println("My Int is: " + myInt_1);
        System.out.println("My Int is: " + myInt_2);
        System.out.println("My Int is: " + myInt_3);
    }
}
